void solve(){
    int n, q;
    cin >> n >> q;
    vector<long long> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    vector<long long> frequency(n + 1, 0);
    
   
    for (int i = 0; i < q; i++) {
        int l, r;
        cin >> l >> r;
        frequency[l - 1]++;
        if (r < n) frequency[r]--;
    }
    
   
    for (int i = 1; i < n; i++) {
        frequency[i] += frequency[i - 1];
    }
    
  
    frequency.pop_back();
    
   
    sort(a.begin(), a.end(), greater<long long>());
    sort(frequency.begin(), frequency.end(), greater<long long>());
    
    
    long long maxSum = 0;
    for (int i = 0; i < n; i++) {
        maxSum += a[i] * frequency[i];
    }
    
    cout << maxSum << endl;
}

