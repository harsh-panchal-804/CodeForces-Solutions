void solve(){
    ll k;
    cin >> k;
    vector<int> groups;
    int totalPoints = 0;
    while(k > 0){
        int best = 2;
        int cap = 500 - totalPoints; 
        for(int m = 2; m <= cap; m++){
            ll tri = (ll)m*(m-1)/2;
            if(tri <= k)
                best = m;
            else
                break;
        }
        groups.push_back(best);
        totalPoints += best;
        k -= (ll)best*(best-1)/2;
    }
    cout << totalPoints << "\n";
    ll curX = 0;
    ll curY = 0;
    for(auto groupSize : groups){
        curY++; 
        for (int i = 0; i < groupSize; i++){
            cout << curX + i << " " << curY << "\n";
        }
       
        curX += groupSize;
}}

