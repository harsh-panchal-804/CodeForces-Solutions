vector<bool> Reach(105, false);
vector< pair<int,int> > Int;
 
void dfs(int n)
{
    Reach[n] = true;
 
    for(int i = 1; i < Int.size(); i++)
        if(!Reach[i])
            if((Int[n].first > Int[i].first && Int[n].first < Int[i].second) || (Int[n].second > Int[i].first && Int[n].second < Int[i].second))
                dfs(i);
}


